package com.github.jimorc.flexishowbuilder;

/**
 * IPLColumn enumerates the columns in an ImageAndPersonLine object.
 */
public enum IPLColumn {
    /**
     * IMAGE_FILE_NAME is the image file name column.
     */
    IMAGE_FILE_NAME,
    /**
     * IMAGE_TITLE is the image title column.
     */
    IMAGE_TITLE,
    /**
     * PERSON_FULL_NAME is the person's full name column.
     */
    PERSON_FULL_NAME,
    /**
     * PERSON_FIRST_NAME is the person's first name column.
     */
    PERSON_FIRST_NAME,
    /**
     * PERSON_LAST_NAME is the person's last name column.
     */
    PERSON_LAST_NAME
}
